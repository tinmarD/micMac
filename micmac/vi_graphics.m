% [varargout] = VI_GRAPHICS (varargin)
% Returns the default values of parameters specified as input. 
% ex: [obstimet_min, obstimet_max] = ...
%                   vi_defaultval ('obstimet_min','obstimet_max');
%
% See also vi_defaultval




graphicsvals = {'figposition',          [200, 200, 1100, 700];...  
                'gridcolorstacked',     [0.5,0.5,1];...
                'plotcolor',            [0,0,1];...
                'plotcolors',           [0.08,0.17,0.55; 0.8,0.75,0; 0,0.8,0.5; 0.5,0.5,0.5;...
                                        1,0,1; 1,0.7,0.4; 0,0,1; 1,0,0;...
                                        1,0.6,0.7];...
                'tf_grid_color',        [0.4,0.4,0.4];...
                'chancorrcolors',       [0,0,1; 1,0,0; 0,1,0; ...
                                        1,0.1,0.7; 1,0.8,0; 0,0.35,0; 0.5,0.5,1;...
                                        0.6,0.31,0.28; 0,1,0.75];...
                'fontsize_base',        8;...
                'eventcolor',    [0.08,0.17,1];...
                'shadoweventcolor',     [0.9,0.17,0.06];...
                'eventlinewidth',       1;...
                'eventalphaval',        0.2;...
                'eventalphavalshadow',  0.07;...
                'eventseledgecolor',    [0.1,0.2,0.55];...
                'scaleampcolor',        [1,0,0];...
                'addeventcolor',        [1,0.6,0.6];...
                'artifacteventcolor',   [1,0.7,0.4];...         
                'colormaps',            {'Jet','HSV','Hot','Cool','Spring','Summer','Autumn','Winter','Gray','Bone','Copper','Pink'};
               };

           
if strcmpi(vi_defaultval('colorscheme'),'dark') 
    graphicsvals = [graphicsvals;...
       {
       'backgroundcolor',       (1/255)*[7,54,66];...
       'panelbackcolor',        (1/255)*[7,54,66];...      
       'edgecolor',          	(1/255)*[38,139,210];...    % blue
       'edgecolorshadow',   	(1/255)*[42,161,152];...    % cyan
       'textcolor',          	(1/255)*[131,148,150];...   % base 0
       'buttonbackcolor',       (1/255)*[7,54,66];...       % base 02
       'buttonforecolor',       (1/255)*[203,75,22];...     % orange
       'axisbackcolor',         (1/255)*[255,255,255];...   % 
       'channeltextcolor',      (1/255)*[38,139,210];...    % blue
       'plotbackgroundcolor',   (1/255)*[255,255,255];...   % base 3
       'xgridcolor',            [0,0,0];...
       'ygridcolor',            [0,0,1];...
       'xtickcolor',            (1/255)*[131,148,150];...   % base 0
       'ytickcolor',            (1/255)*[131,148,150];...   % base 0
       'plotchannelcolor',      (1/255)*[38,139,210];...    % blue
       'eeglabpopupbackcolor',  (1/255)*[0,43,54];...       % base 03
       'signalnamecolor',       (1/255)*[38,139,210];...    % blue
       'waitbarbackcolor',      (1/255)*[0,43,54];...       % base 03
        }];
else % 'light' theme
    graphicsvals = [graphicsvals;...
       {
       'backgroundcolor',       [0.8,0.8,0.8];...
       'textcolor',             [0,0,0];...
       'plotbackgroundcolor',   [1,1,1];...
       'xgridcolor',            [0,0,0];...
       'ygridcolor',            [0,0,1];...
       'xtickcolor',            [0,0,0];...     
       'ytickcolor',            [0,0,0];...     
       'plotchannelcolor',   	[0,0,0];...    
       'eeglabpopupbackcolor',  [.66 .76 1];...             
       'signalnamecolor',       (1/255)*[38,139,210];...    % blue
       'waitbarbackcolor',      [0.8,0.8,0.8];...       
        }];     
end
            
varargout   = cell(1,nargin);

for i=1:length(varargin)
    input_i = varargin{i};
    if ~ischar(input_i)
        error ('input must be a string');
    end

    inputpos = nonzeros (strcmp(input_i,graphicsvals(:,1)) .* ((1:size(graphicsvals,1)).'));
    if isempty(inputpos)
        error (['Could not find the default value for the parameter named: ',...
                   input_i]);
    else
        varargout(i) = graphicsvals(inputpos,2);
    end
end


end



